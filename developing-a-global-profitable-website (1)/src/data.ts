// Build simple question from compact format
function buildQ(q: string, qt: string, opts: string[], optst: string[], results: Record<string, string>[]) {
  return {
    question: { en: q, tr: qt, es: q, fr: q, de: q, pt: q },
    options: opts.map((o, i) => ({ en: o, tr: optst[i], es: o, fr: o, de: o, pt: o })),
    results,
  };
}

function expandQuizzes(quizList: Quiz[]): Quiz[] {
  return quizList.map(quiz => {
    // First add hand-written extras for personality & love
    const handWritten = extraQuestions[quiz.id];
    let allQs = handWritten ? [...quiz.questions, ...handWritten] : [...quiz.questions];

    // Then add compact extras for knowledge/career/spirit/psychology
    const compact = moreExtras[quiz.id];
    if (compact) {
      const baseResults = quiz.questions[0].results;
      compact.forEach(c => {
        allQs.push(buildQ(c.q, c.qt, c.opts, c.optst, baseResults));
      });
    }

    // For quizzes still under 10, generate unique variations
    if (allQs.length < 10) {
      const needed = 10 - allQs.length;
      const variations = [
        { en: "What do you value most?", tr: "En çok neye değer verirsin?" },
        { en: "Your friends would say you are:", tr: "Arkadaşların seni nasıl tanımlar:" },
        { en: "On a road trip, you:", tr: "Yolculukta sen:" },
        { en: "Your morning routine includes:", tr: "Sabah rutinin:" },
        { en: "Pick a color that speaks to you:", tr: "Sana hitap eden renk:" },
        { en: "Your biggest strength is:", tr: "En büyük gücün:" },
        { en: "When bored, you:", tr: "Sıkıldığında:" },
        { en: "Choose your ideal weather:", tr: "İdeal havanı seç:" },
        { en: "Your social media style:", tr: "Sosyal medya tarzın:" },
        { en: "What keeps you up at night?", tr: "Seni gece uyanık tutan ne?" },
      ];
      const baseResults = quiz.questions[0].results;
      const baseOptions = quiz.questions.map(q => q.options);
      for (let i = 0; i < needed; i++) {
        const vi = (allQs.length + i) % variations.length;
        const oi = (allQs.length + i) % baseOptions.length;
        // Shuffle options for variety
        const shuffled = [...baseOptions[oi]];
        for (let j = shuffled.length - 1; j > 0; j--) {
          const k = (j + allQs.length + i) % (j + 1);
          [shuffled[j], shuffled[k]] = [shuffled[k], shuffled[j]];
        }
        // Shuffle results too
        const shuffledR = [...baseResults];
        for (let j = shuffledR.length - 1; j > 0; j--) {
          const k = (j + allQs.length + i + 2) % (j + 1);
          [shuffledR[j], shuffledR[k]] = [shuffledR[k], shuffledR[j]];
        }
        allQs.push({
          question: { en: variations[vi].en, tr: variations[vi].tr, es: variations[vi].en, fr: variations[vi].en, de: variations[vi].en, pt: variations[vi].en },
          options: shuffled,
          results: shuffledR,
        });
      }
    }

    return { ...quiz, questions: allQs, time: Math.ceil(allQs.length * 0.4) };
  });
}

export const expandedQuizzes = expandQuizzes(quizzes);
